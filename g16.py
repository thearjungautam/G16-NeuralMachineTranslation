def ids_to_tokens(ids, itos, eos_idx, pad_idx, sos_idx):
    tokens = []
    for idx in ids:
        if idx == eos_idx:
            break
        if idx in (pad_idx, sos_idx):
            continue
        tokens.append(itos.get(idx, "<unk>"))
    return tokens

@torch.no_grad()
def compute_bleu(model, dataloader, trg_itos, device, max_len=50):
    model.eval()

    predictions = []
    references = []

    for batch in tqdm(dataloader, desc="BLEU", leave=False):
        src = batch["src"].to(device, non_blocking=True)
        src_lengths = batch["src_lengths"].to(device, non_blocking=True)
        trg = batch["trg"].to(device, non_blocking=True)

        pred_ids = model.greedy_decode(src, src_lengths, max_len=max_len)

        for i in range(src.size(0)):
            pred_tokens = ids_to_tokens(
                pred_ids[i].tolist(),
                trg_itos,
                eos_idx=TRG_EOS_IDX,
                pad_idx=TRG_PAD_IDX,
                sos_idx=TRG_SOS_IDX
            )

            gold_tokens = ids_to_tokens(
                trg[i].tolist(),
                trg_itos,
                eos_idx=TRG_EOS_IDX,
                pad_idx=TRG_PAD_IDX,
                sos_idx=TRG_SOS_IDX
            )

            predictions.append(" ".join(pred_tokens))
            references.append(" ".join(gold_tokens))

    bleu = sacrebleu.corpus_bleu(predictions, [references])
    return bleu.score, predictions, references

